"""
------------------------------- Задание -------------------------------
n! означает n × (n − 1) × ... × 3 × 2 × 1

Например, 10! = 10 × 9 × ... × 3 × 2 × 1 = 3628800,
и сумма цифр в числе 10! равна 3 + 6 + 2 + 8 + 8 + 0 + 0 = 27.

Найдите сумму цифр в числе 100!.

"""

from math import factorial


def sum_val(x):
    sum_x = 0
    while x > 0:
        sum_x += x % 10
        x //= 10
    return sum_x

n = 100
x = factorial(n)
print(__doc__)
print("Ответ:", sum_val(x))
